module.exports = {
  sayHello: function(firstName, lastName) {
    return 'Hello, ' + firstName + ' ' + lastName + '!'
  },
  sayGoodbye: function(firstName, lastName) {
    return 'Goodbye, ' + firstName + ' ' + lastName + '!'
  },
  sayAloha: function(firstName, lastName) {
    return 'Aloha, ' + firstName + ' ' + lastName + '!'
  }
}
